#include "allInclude.h"

void main() {
    chessPos initialPos;
    pathTree tr;
    char     buffer[50];
    
    scanf("%s", buffer);
    int size = strlen(buffer);
    
    if (size != 2)                            // incase the input longer then expected
    {
        printf("Invalid input");
        exit(1);
    }

    initialPos[0] = buffer[0];
    initialPos[1] = buffer[1];

    if (isValidMove(initialPos) == false)    //incase the position given isn't valid
    {
        printf("Invalid input");
        exit(1);
    }
    else
    {
        tr = findAllPossibleKnightPaths(initialPos);
        chessPosList* lst = (chessPosList*)malloc(sizeof(chessPosList));
        lst = findKnightPathCoveringAllBoard(&tr);
        if (lst->head == NULL)
            printf("No knight's tour");
        else
            printTable(*lst);
    }

    freeTree(&tr);
}
